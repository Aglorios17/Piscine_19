void	ft_rev_int_tab(int *tab, int size)
{
	int a;
	int tmp;

	a = 0;
	tmp = 0;
	while (a < size / 2)
	{
		tmp = tab[a];
		tab[a] = tab[size - 1 - a];
		tab[size - 1 - a] = tmp;
		a++;
	}
}
