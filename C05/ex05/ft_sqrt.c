int	ft_sqrt(int nb)
{
	int a;
	int nb2;

	nb2 = nb;
	a = 1;
	while (nb2 > a)
	{
		nb2 = (nb2 + a) / 2;
		a = nb / nb2;
	}
	if (nb2 * nb2 == nb)
		return (nb2);
	return (0);
}
