int	ft_str_is_alpha(char *str)
{
	int a;

	a = 0;
	while (str[a] != '\0')
	{
		if ((str[a] > 64 && str[a] < 91) || (str[a] > 96 && str[a] < 123))
			a++;
		else
		{
			return (0);
			str[a] = '\0';
		}
	}
	return (1);
}
