#include <unistd.h>

int	main(int argc, char **argv)
{
	int a;
	int b;

	a = 1;
	while (argc > a)
	{
		b = 0;
		while (argv[a][b])
		{
			write(1, &argv[a][b], 1);
			b++;
		}
		write(1, "\n", 1);
		a++;
	}
	return (0);
}
